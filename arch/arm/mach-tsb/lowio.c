#include <stdint.h>
#include <stdbool.h>
#include <stdio.h>

#include <asm/hwio.h>
#include "chip.h"

#define UART_RBR_THR_DLL            (UART_BASE + 0x0)
#define UART_LSR                    (UART_BASE + 0x14)

#define UART_LSR_THRE               (1 << 5)

void machine_lowputchar(char c)
{
    while ((read32(UART_LSR) & UART_LSR_THRE) != UART_LSR_THRE);
    write32(UART_RBR_THR_DLL, c);
}
